package com.hanshows.cdi.pojo;

public class FileInfo{
	
	private String fileName;
	private String fileMd5;
	private String fileSerial;
	private String fileOperation;
	private String filePath;
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileMd5() {
		return fileMd5;
	}
	public void setFileMd5(String fileMd5) {
		this.fileMd5 = fileMd5;
	}
	public String getFileSerial() {
		return fileSerial;
	}
	public void setFileSerial(String fileSerial) {
		this.fileSerial = fileSerial;
	}
	public String getFileOperation() {
		return fileOperation;
	}
	public void setFileOperation(String fileOperation) {
		this.fileOperation = fileOperation;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
}
